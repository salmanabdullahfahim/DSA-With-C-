// Operators
// 1. Arithmetic - +, -, *, /, %, ++, --


#include<iostream>
using namespace std;
int main(){

    int a = 10, b = 5;

    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << a % b << endl;
    cout << a++ << endl;
    cout << a-- << endl;
}
